package sample;

public class Rechner {

    public int rechnePlus(int zahl1, int zahl2) {
        return zahl1 + zahl2;
    }
    public int rechneMinus(int zahl1, int zahl2) {
        return zahl1 - zahl2;
    }
    public int rechneMal(int zahl1, int  zahl2) {
        return zahl1 * zahl2;
    }
    public int rechneGeteilt(int zahl1, int zahl2) {
        return zahl1 / zahl2;
    }
    public int rechneModulo(int zahl1, int zahl2) {
        return zahl1 % zahl2;
    }
}
